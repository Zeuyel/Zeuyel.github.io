    # Aliases
    CALLOUT_ALIASES = {
      'hint' => 'tip', 'important' => 'warning', 'caution' => 'warning',
      'error' => 'danger', 'cite' => 'quote', 'summary' => 'abstract',
      'tldr' => 'abstract', 'check' => 'success', 'done' => 'success',
      'help' => 'question', 'faq' => 'question', 'fail' => 'failure',
      'missing' => 'failure',
    }
  end

  # Hook into the HTML output after rendering
  Jekyll::Hooks.register [:pages, :documents], :post_render do |doc|
    next unless doc.output

    # Match: <blockquote>\n  <p>[!type]... content</p>\n  (optional more elements)\n</blockquote>
    doc.output = doc.output.gsub(%r{<blockquote>\s*<p>\[!(\w+)\]([+-]?)[ \t]*(.*?)</p>(.*?)</blockquote>}m) do
      raw_type = $1.downcase
      fold = $2  # + or - or empty
      first_line = $3.strip
      rest = $4.strip

      css_type = ObsidianCallouts::CALLOUT_ALIASES[raw_type] || raw_type
      icon = ObsidianCallouts::CALLOUT_ICONS[css_type] || ObsidianCallouts::CALLOUT_ICONS['note']

      # Split first_line: "Title\nBody lines" â€” title is before first \n or <br>
      parts = first_line.split(/\n|<br\s*\/?>/, 2)
      title = parts[0]&.strip
      title = raw_type.capitalize if title.nil? || title.empty?
      extra_body = parts[1]&.strip || ''

      # Build body content
      body_parts = []
      body_parts << "<p>#{extra_body}</p>" unless extra_body.empty?
      body_parts << rest unless rest.empty?
      body_html = body_parts.join("\n")

      if !fold.empty?
        # Foldable callout
        open_attr = fold == '+' ? ' open' : ''
        <<~HTML
          <details class="callout callout-#{css_type}"#{open_attr}>
            <summary class="callout-title">
              <div class="callout-icon">#{icon}</div>
              <div class="callout-title-inner">#{title}</div>
              <div class="callout-fold"><svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="m6 9 6 6 6-6"/></svg></div>
            </summary>
            <div class="callout-content">#{body_html}</div>
          </details>
        HTML
      else
        <<~HTML
          <div class="callout callout-#{css_type}">
            <div class="callout-title">
              <div class="callout-icon">#{icon}</div>
              <div class="callout-title-inner">#{title}</div>
            </div>
            <div class="callout-content">#{body_html}</div>
          </div>
        HTML
      end
    end
  end
